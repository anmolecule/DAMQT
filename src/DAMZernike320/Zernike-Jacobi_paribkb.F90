                *(9.3968347936601905D-8+a*(1.2529113058213587D-8+a*(1.5661391322766984D-9+a&
                *(1.8425166262078805D-10+a*(2.0472406957865339D-11+a*(2.1549902060910883D-12+a&
                *(2.1549902060910883D-13+a*(2.0523716248486555D-14+a*(1.8657923862260505D-15+a&
                *(1.6224281619356961D-16)))))))))))))))))))))))
        bsi3med = expza * (0.33333333333333333D0+a*(0.33333333333333333D0+a*(0.20000000000000000D0+a&
                *(0.088888888888888889D0+a*(0.031746031746031746D0+a*(0.0095238095238095238D0+a&
                *(0.0024691358024691358D0+a*(0.00056437389770723104D0+a*(0.00011544011544011544D0+a&
                *(0.000021377799155576933D0+a*(3.6177813955591733D-6+a*(5.6381008761961143D-7+a&
                *(8.1439234878388318D-8+a*(1.0962973925936889D-8+a*(1.3818874696559104D-9+a&
                *(1.6377925566292271D-10+a*(1.8317416751774250D-11+a*(1.9394911854819795D-12+a&
