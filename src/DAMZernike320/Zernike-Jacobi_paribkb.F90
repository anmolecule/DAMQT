  subroutine bibk3med(z1,z2,bsi1med,bsi3med)
    implicit none
    real(8) :: a, bsi1med, bsi3med, expza, expzb, z1, z2, za, zb
!	Fitting of functions:
!		bsi(i) = Sqrt[Pi/2] * Exp[-(za+zb)] * (za-zb)**(-i-1/2) * BesselI[i+1/2,za-zb)
!			  = khat[1/2,za+zb] * (za-zb)**(-i-1/2) * BesselI[i+1/2,za-zb)
!	corresponding to  i = 0  and  i = 1
    if (z1 .ge. z2) then
        za = z1
        zb = z2
    else
        za = z2
        zb = z1
    endif
    a = za - zb
    if (zb .ge. 200.d0 .or. a .ge. 1.d8) then
        bsi1med = 0.d0
        bsi3med = 0.d0
        return
    endif
    if (za .gt. 200.d0) then
        expza = 0.d0
    else
        expza = exp(-(za+za))
    endif
    if (a .ge. 1.d0) then
        expzb = exp(-(zb+zb))
        bsi1med = .5d0 * (expzb - expza) / a
        bsi3med = .5d0 * (expzb*(a-1.d0)  + expza*(a+1.d0))/ a**3
    else
        bsi1med = expza * (1.D0+a*(1.D0+a*(0.66666666666666667D0+a*(0.33333333333333333D0+a&
